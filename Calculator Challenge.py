# Calculator for adding, subtracting, multiplying, and dividing integers.

def add(number1, number2):
    s_number = number1 + number2
    return s_number


def subtract(number1, number2):
    r_number = number1 - number2
    return r_number


def multiply(number1, number2):
    m_number = number1 * number2
    return m_number


def divide(number1, number2):
    q_number = number1 / number2
    return q_number



